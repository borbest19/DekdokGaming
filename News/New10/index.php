<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;DayZ เกมเอาตัวรอดจากซอมบี้ จาก Bohemia Interactive ผู้พัฒนาเกม Arma กำลังจะได้หลุดออกจาก Early Access แล้วในปีหน้า
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;โดยในบลอคของทางผู้พัฒนาระบุว่า ปี 2018 จะเป็นปีที่น่าตื่นเต้นที่สุดของ DayZ เพราะนอกจากการหลุดออกจาก Early Access กลายเป็นเกมเต็มรุปแบบ ของ DayZ แล้ว ตัวเกมจะยังก้าวเข้าสู่การลงให้กับเครื่องคอนโซลด้วย
<br>
